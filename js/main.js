// Database

require('dotenv').config();
const express = require('express');
const mysql = require('mysql2');
const multer = require('multer');
const cors = require('cors');
const path = require('path');

const app = express();
app.use(express.json());
app.use(cors());
app.use('/uploads', express.static('uploads'));

// Database Connection
const db = mysql.createConnection({
    host: 'localhost',
    user: 'root',
    password: '1234',  // Change this to your MySQL password
    database: 'media_submissions'
});

db.connect(err => {
    if (err) console.error('Database connection failed:', err);
    else console.log('Connected to MySQL');
});

// Multer Setup for File Uploads
const storage = multer.diskStorage({
    destination: './uploads/',
    filename: (req, file, cb) => {
        cb(null, Date.now() + path.extname(file.originalname));
    }
});
const upload = multer({ storage });

// Route to Handle Form Submission
app.post('/submit', upload.single('file_upload'), (req, res) => {
    const { name, company, email, category, submission_date, comments } = req.body;
    const file_path = req.file ? `/uploads/${req.file.filename}` : null;

    if (!file_path) {
        return res.status(400).json({ error: 'File upload is required.' });
    }

    const sql = `INSERT INTO submissions (name, company, email, category, submission_date, comments, file_path)
                 VALUES (?, ?, ?, ?, ?, ?, ?)`;

    db.query(sql, [name, company, email, category, submission_date, comments, file_path], (err, result) => {
        if (err) return res.status(500).json({ error: err.message });
        res.json({ message: 'Submission successful', id: result.insertId, file_path });
    });
});

// Start Server
app.listen(5000, () => console.log('Server running on port 5000'));

